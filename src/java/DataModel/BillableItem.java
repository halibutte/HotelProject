package DataModel;

/**
 *
 * @author x3041557
 */
public class BillableItem {
    private String code;
    private String name;
    private double price;

    public BillableItem() {
    }

    public BillableItem(String code, String name, double price) {
        this.code = code;
        this.name = name;
        this.price = price;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
}